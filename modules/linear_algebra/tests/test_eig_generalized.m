0.0366 - 0.0923i   0.4278 + 0.1844i];
D_REF = [0.7075 + 0.4673i   0.0000 + 0.0000i;
0.0000 + 0.0000i   0.1947 - 0.6737i];
assert_isapprox(V, V_REF, 1e-4);
assert_isapprox(D, D_REF, 1e-4);
%=============================================================================
