x = [2 2 2];
n = vecnorm(x);
assert_isapprox(n, 3.4641, 1e-4);
%=============================================================================
n = vecnorm(x, 1);
assert_isapprox(n, 6, 1e-4);
%=============================================================================
A = [2 0 1;-1 1 0;-3 3 0];
n = vecnorm(A);
assert_isapprox(n, [ 3.7417    3.1623    1.0000], 1e-4);
%=============================================================================
A = [0 1 2; 3 4 5];
n = vecnorm (A);
assert_isapprox(n, [3.0000    4.1231    5.3852], 1e-4);
%=============================================================================
A = [0 1 2; 3 4 5];
n = vecnorm (A, 2, 2);
assert_isapprox(n, [2.2361; 7.0711], 1e-4);
%=============================================================================
A = [0 1 2; 3 4 5];
n = vecnorm (A, Inf);
assert_isapprox(n, [3     4     5], 1e-4);
