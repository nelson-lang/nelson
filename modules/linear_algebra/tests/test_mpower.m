A = [1 2; 3 4];
R = A^2;
R2 = A*A;
REF = [7 10; 15 22];
assert_isapprox(R, REF, 1e-4);
assert_isapprox(R2, REF, 1e-4);
%=============================================================================
R = 2^6;
REF = 64;
assert_isapprox(R, REF, 1e-4);
%=============================================================================
R = 0.25^pi;
REF = 0.012840228555487;
assert_isapprox(R, REF, 1e-4);
%=============================================================================
R = [1 2 3; 2 2 3; 3 2 4]^0;
REF = eye(3, 3);
