addpath([nelsonroot(), '/modules/core/tests/']);
%=============================================================================
assert_isequal(nargin('inputname'), 1);
assert_isequal(nargout('inputname'), 1);
%=============================================================================
A = 1;
B = 2;
C = 3;
R = inputname_1(A, B, C);
REF = ["A", "B", "C"];
assert_isequal(R, REF);
%=============================================================================
R = inputname_1(A, 33, C);
REF = ["A", "", "C"];
assert_isequal(R, REF);
%=============================================================================
A = ones(3, 3);
C = {'a', 'b'; 'c', 'd'};
R = inputname_1(A(1,1), {'a', 'b'; 'c', 'd'}, C{2, 2});
REF = ["", "", ""];
assert_isequal(R, REF);
