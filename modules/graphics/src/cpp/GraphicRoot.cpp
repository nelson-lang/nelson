#include <QtGui/QWindow>
#include "GraphicRoot.hpp"
#include "MainGuiObject.hpp"
#include "GORoot.hpp"
//=============================================================================
namespace Nelson {
//=============================================================================
static QWindow* parent = nullptr;
//=============================================================================
void
createGraphicRoot()
{
    parent = static_cast<QWindow*>(GetMainGuiObject());
}
//=============================================================================
void*
getGraphicRoot()
{
    auto* groot = new GORoot(parent);
    return (void*)groot;
}
//=============================================================================
} // namespace Nelson
