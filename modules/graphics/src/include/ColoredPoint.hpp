class coloredPoint
{
public:
    //=============================================================================
    double x;
    double y;
    double z;
    double r;
    double g;
    double b;
    double a;
    //=============================================================================
    coloredPoint(double _x, double _y, double _z, double _r, double _g, double _b, double _a)
    {
        x = _x;
        y = _y;
        z = _z;
        r = _r;
        g = _g;
        b = _b;
        a = _a;
    }
    //=============================================================================
};
