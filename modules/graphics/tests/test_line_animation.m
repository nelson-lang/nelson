figure;
H = line(0,0);
axis([0 2*pi -1 1]);
grid on;
x = linspace(0, 2*pi, 200);
y = sin(x);
for k = 1:length(x)
    H.XData = x(1:k);
    H.YData = y(1:k);
    drawnow;
end
