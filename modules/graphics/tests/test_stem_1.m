f = figure();
x1 = linspace(0, 2 * pi, 50)';
x2 = linspace(pi, 3 * pi, 50)';
X = [x1, x2];
Y = [cos(x1), 0.5 * sin(x2)];
h = stem(X, Y);
%=============================================================================
f = figure();
x1 = linspace(0, 2 * pi, 50)';
x2 = linspace(pi, 3 * pi, 50)';
X = [x1, x2];
Y = [cos(x1), 0.5*sin(x2)];
h = stem(X, Y, 'filled');
assert_isequal(size(h), [1 2]);
