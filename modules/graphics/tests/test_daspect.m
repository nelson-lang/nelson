figure();
daspect([1 2 1]);
d = daspect();
assert_isequal(d, [1 2 1]);
%=============================================================================
figure();
daspect([1 1 1]);
d = daspect();
assert_isequal(d, [1 1 1]);
%=============================================================================
figure();
daspect([2 1 1]);
d = daspect();
assert_isequal(d, [2 1 1]);
%=============================================================================  
figure();
daspect([1 1 2]);
d = daspect();
assert_isequal(d, [1 1 2]);
%=============================================================================
figure();
daspect('auto');
d = daspect();
assert_isequal(d, [1 1 1]);
m = daspect('mode');
assert_isequal(m, 'auto');
