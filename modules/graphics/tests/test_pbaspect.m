figure();
pbaspect([1 2 1]);
a = pbaspect();
assert_isequal(a, [1 2 1]);
%=============================================================================
figure();
[x,y] = meshgrid(-2:.2:2);
z = x.*exp(-x.^2 - y.^2);
surf(x,y,z)
pbaspect([2 1 1])
a = pbaspect();
assert_isequal(a, [2 1 1]);
%=============================================================================
m = pbaspect('mode');
assert_isequal(m, 'manual');
%=============================================================================
pbaspect('auto');
a = pbaspect();
assert_isequal(a, [1 1 1]);
m = pbaspect('mode');
assert_isequal(m, 'auto');
