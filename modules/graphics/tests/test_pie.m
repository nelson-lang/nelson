f1 = figure();
p = pie ([3, 2, 1], [0, 0, 1]);
assert_isequal(size(p), [1 6]);
%=============================================================================
f2 = figure();
p = pie([5 9 4 6 3],[0 1 0 1 0]);
assert_isequal(size(p), [1 10]);
%=============================================================================
f3 = figure();
p = pie([3 4 6 2],[0 1 0 0],["part1", "part2", "part3", "part4"]);
assert_isequal(size(p), [1 8]);
%=============================================================================
f4 = figure();
y2010 = [50 0 100 95];
y2011 = [65 22 97 120];
ax1 = subplot(1, 2, 1);
p1 = pie(ax1, y2010)
title('2010')
ax2 = subplot(1, 2, 2);
p2 = pie(ax2, y2011)
title('2011')
assert_isequal(size(p1), [1 8]);
assert_isequal(size(p2), [1 8]);
