A = zeros(2, 1, 2);
A(:, :, 1) = [1 2]';
A(:, :, 2) = [3 4]';
R = squeeze(A);
REF = [1     3; 2     4];
assert_isequal(R, REF)
%=============================================================================
A = zeros(1, 1, 3);
A(:, :, 1:3) = [1 2 3];
R = squeeze(A);
REF = [1; 2; 3];
assert_isequal(R, REF)
