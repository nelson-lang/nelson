A = 'no word, no bond, row on.';
B = flipud(A);
assert_isequal(B, A);
%=============================================================================
A = eye(2, 3);
B = flipud(A);
REF = [ 0     1     0;1     0     0];
assert_isequal(B, REF);
%=============================================================================
A = [];
A(1:2,1:2,1) = [5 6; 7 8];
A(1:2,1:2,2) = [1 2; 3 4];
B = flipud(A);
REF = [];
REF(1:2,1:2,1) = [7 8;5 6];
REF(1:2,1:2,2) = [3 4;1 2];
assert_isequal(B, REF);
