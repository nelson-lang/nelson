assert_isequal(nargin('ndgrid'), -1)
assert_isequal(nargout('ndgrid'), -1)
%=============================================================================
assert_checkerror('ndgrid()', _('Not enough input arguments.'));
assert_checkerror('[X, Y, Z, W, K, U]=ndgrid(1, 2, 3, 4, 5)', _('Not enough input arguments.'));
%=============================================================================         
assert_isequal(ndgrid(1, 2, 3, 4, 5), 1);
%=============================================================================         
[X, Y] = ndgrid(1, 2);
assert_isequal(X, 1);
assert_isequal(Y, 2);
%=============================================================================    
x = 1:3;
y = 4:6;
[X, Y] = ndgrid(x, y);
assert_isequal(X, [1 1 1; 2 2 2; 3 3 3]);
assert_isequal(Y, [4 5 6; 4 5 6; 4 5 6]);
%=============================================================================
x = 1:2;
y = 3:4;
z = 5:6;
[X, Y, Z] = ndgrid(x, y, z);
X_REF = [];
X_REF(:,:,1) = [     1     1;     2     2];
X_REF(:,:,2) = [     1     1;     2     2];
Y_REF = [];
Y_REF(:,:,1) = [     3     4;     3     4];
Y_REF(:,:,2) = [     3     4;     3     4];
Z_REF = [];
Z_REF(:,:,1) = [     5     5;     5     5];
Z_REF(:,:,2) = [     6     6     6     6];
assert_isequal(X, X_REF);
assert_isequal(Y, Y_REF);
assert_isequal(Z, Z_REF);
