R = pascal(5);
REF = [    1     1     1     1     1;
     1     2     3     4     5;
     1     3     6    10    15;
     1     4    10    20    35;
     1     5    15    35    70];
assert_isequal(R, REF);
%=============================================================================
R = pascal(3,1);
REF = [     1     0     0;
     1    -1     0;
     1    -2     1];
assert_isequal(R, REF);
%=============================================================================
R = pascal(3,2);
REF = [   1     1     1;
    -2    -1     0;
     1     0     0];
assert_isequal(R, REF);
%=============================================================================
R = pascal(3,2, 'single');
REF = single([   1     1     1;
    -2    -1     0;
     1     0     0]);
assert_isequal(R, REF);
