A = [5, 1, 4, 3];
k = 2;
[val, idx] = maxk (A, k);
assert_isequal(val, [5 4]);
assert_isequal(idx, [1 3]);
%=============================================================================
A = [5; 1; 4; 3];
[val, idx] = maxk (A, 2);
assert_isequal(val, [5; 4]);
assert_isequal(idx, [1; 3]);
%=============================================================================
A = [1 4 2; 7 3 5]; % 2x3
[val, idx] = maxk (A, 1);
assert_isequal(val, [7 4 5]);
assert_isequal(idx, [2 1 2]);
%=============================================================================
[val, idx] = maxk (A, 2, 2);
assert_isequal(val, [4 2; 7 5]);
assert_isequal(idx, [2 3; 1 3]);
%=============================================================================
cmd = 'R = maxk ([1, 2, 3], 5);';
assert_checkerror(cmd, _('Number of elements exceeds size of array.'));
