A = [0 1;0 0];
B = [0;0.2];
p = [-4+j*5; -4-j*5];
K = acker(A,B,p)
REF = [ 205    40];
assert_isequal(K, REF);
%=============================================================================
A = [0 1; 1 1.5];
B = [0 1]';
R = acker(A, B, [0.5,0.5]);
REF = [ 1.2500    0.5000];
assert_isapprox(R, REF, 1e-4);
%=============================================================================
A = [-1 0; 1 -2];
B = [1; 0];
desiredPoles = [-2 + 1i, -2 - 1i];
K = acker(A, B, desiredPoles);
REF = [1 1];
assert_isequal(K, REF);
