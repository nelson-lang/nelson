A = [1  1; 4 -2];
B = [1 -1; 1 -1];
Co = ctrb(A, B);
REF = [     1    -1     2    -2; 1    -1     2    -2]
assert_isequal(Co, REF);
%=============================================================================
sys = ss(A, B, [], []);
Co = ctrb(sys);
REF = [     1    -1     2    -2; 1    -1     2    -2]
assert_isequal(Co, REF);
%=============================================================================
R = ctrb([1, 0; 0, -0.5], [8; 8]);
REF = [ 8     8; 8    -4];
assert_isequal(R, REF);
%=============================================================================
A = [1 2; 0 3];
B = [1; 1];
C = eye(2);
D = zeros(2, 1);
sys = ss(A, B, C, D);
Co = ctrb(sys);
REF = [1 3; 1 3];
assert_isequal(Co, REF);
