numerator = {[2, 0], [1, 3]};
denominator = {[4, 0, 3, -1], [1 , 3, 5]};
sys = tf(numerator, denominator);
z = 1 + j;
frsp = evalfr(sys, z);
REF = [0.0637-0.2166i,   0.4157-0.1348i];
assert_isapprox(frsp, REF, 1e-3);
