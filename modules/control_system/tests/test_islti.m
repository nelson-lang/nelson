assert_isequal(nargin('islti'), 1);
assert_isequal(nargout('islti'), 1);
%=============================================================================
A = [-10 0 0; 10/2 -1 0; 10/2 0 -1];
B = [10 0; 0 -10; 0 10];
C = [0 0 1; 1 1 0];
D = zeros(size(C, 1), size(B, 2));
Ts = 0.2;
S = ss(A, B, C, D, Ts);
assert_istrue(islti(S));
%=============================================================================
A = 1;
assert_isfalse(islti(A));
