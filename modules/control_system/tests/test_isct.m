assert_isequal(nargin('isct'), 1);
assert_isequal(nargout('isct'), 1);
%=============================================================================
A = [-10 0 0; 10/2 -1 0; 10/2 0 -1];
B = [10 0; 0 -10; 0 10];
C = [0 0 1; 1 1 0];
D = zeros(size(C, 1), size(B, 2));
Ts = 0.2;
S = ss(A, B, C, D, Ts);
assert_isfalse(isct(S))
%=============================================================================
A = [-10 0 0; 10/2 -1 0; 10/2 0 -1];
B = [10 0; 0 -10; 0 10];
C = [0 0 1; 1 1 0];
D = zeros(size(C, 1), size(B, 2));
S = ss(A, B, C, D);
assert_istrue(isct(S))
%=============================================================================
S = tf(4.7, [1 -4.43]);
assert_istrue(isct(S));
%=============================================================================
S = tf(4.7, [1 -4.43], 0.5);
assert_isfalse(isct(S));
%=============================================================================
S = tf(3);
assert_istrue(isct(S))
