x = linspace(0, 4 * pi, 10);
y = sin(x);
p = polyfit(x, y, 7);
REF = [-0.0001    0.0028   -0.0464    0.3702   -1.3808    1.9084   -0.1141    0.0002];
assert_isapprox(p, REF, 1e-4);
