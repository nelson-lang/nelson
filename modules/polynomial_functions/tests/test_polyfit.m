x = [1, 2, 3; 4, 5, 6];
y = [0, 0, 1; 1, 0, 0];
p = polyfit (x, y, 5);
REF = [ 0.0000    0.0833   -1.1667    5.4167   -9.3333    5.0000];
assert_isapprox(p, REF, 1e-4);
