p = [30 0 -20 0 10 50];
q = polyder(p);
REF = [ 150     0   -60     0    10];
assert_isequal(q, REF);
%=============================================================================
a = [1 -2 0 0 11];
b = [1 -10 15];
q = polyder(a, b);
REF = [  6   -60   140   -90    22  -110];
assert_isequal(q, REF);
%=============================================================================
q = polyder(33);
REF = 0;
assert_isequal(q, REF);
