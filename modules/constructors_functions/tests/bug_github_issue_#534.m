% diag(ones(0,1),-1) must return a value 0.
%=============================================================================
R = diag(ones(0, 1), -1);
REF = 0;
assert_isequal(R, REF)
%=============================================================================
R = diag(ones(0, 0), -1);
REF = [];
assert_isequal(R, REF)
%=============================================================================
R = diag(ones(1, 0), -1);
REF = 0;
assert_isequal(R, REF)
