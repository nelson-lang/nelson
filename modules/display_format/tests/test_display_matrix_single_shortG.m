%=============================================================================
R = evalc('A = single(eye(3,3) * eps)');
REF = '
A =

  3×3 single matrix

   2.2204e-16            0            0
            0   2.2204e-16            0
            0            0   2.2204e-16

';
assert_isequal(R, REF)
%=============================================================================
A = [0.2220, 1, 2;
     0,  0.2220, 4;
     5, 6, 0.2220];
R = evalc('A = single(A)');
REF = '
A =

  3×3 single matrix

        0.222            1            2
            0        0.222            4
            5            6        0.222

';
assert_isequal(R, REF)
%=============================================================================
