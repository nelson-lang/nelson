format('shortEng')
%=============================================================================
R = evalc('A = {1}');
REF =   '
A =

  1×1 cell array

    {[1.0000e+000]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {pi}');
REF =   '
A =

  1×1 cell array

    {[3.1416e+000]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {NaN}');
REF = '
A =

  1×1 cell array

    {[NaN]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {-Inf}');
REF =  '
A =

  1×1 cell array

    {[-Inf]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {Inf}');
REF =  '
A =

  1×1 cell array

    {[Inf]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {complex(pi, pi)}');
REF =   '
A =

  1×1 cell array

    {[3.1416e+000 +  3.1416e+000i]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {complex(1, 0)}');
REF = '
A =

  1×1 cell array

    {[1.0000e+000 +  0.0000e+000i]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {complex(NaN, NaN)}');
REF = '
A =

  1×1 cell array

    {[NaN +             NaNi]}

';
assert_isequal(R, REF)
%=============================================================================
R = evalc('A = {complex(1.8e99, -eps)}');
REF = '
A =

  1×1 cell array

    {[1.8000e+099 -222.0446e-018i]}

';
assert_isequal(R, REF)
