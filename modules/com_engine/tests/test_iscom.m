assert_isequal(nargin('iscom'), 1);
assert_isequal(nargout('iscom'), 1);
%=============================================================================
pTextToSpeech = actxserver('Sapi.SpVoice');
r = iscom(pTextToSpeech);
assert_istrue(r);
%=============================================================================
delete(pTextToSpeech);
r = iscom(pTextToSpeech);
assert_isfalse(r);
%=============================================================================
r = iscom(1);
assert_isfalse(r);
