assert_isequal(nargin('isfloat'), 1);
assert_isequal(nargout('isfloat'), 1);
%=============================================================================
A = 3;
res = isfloat(A);
assert_istrue(res);
%=============================================================================
A = single(3);
res = isfloat(A);
assert_istrue(res);
%=============================================================================
A = 'Nelson';
res = isfloat(A);
assert_isfalse(res);
%=============================================================================
A = sparse(3);
res = isfloat(A);
assert_istrue(res);
