assert_isequal(nargin('iscell'), 1);
assert_isequal(nargout('iscell'), 1);
%=============================================================================
A = 3;
res = iscell(A);
assert_isfalse(res);
%=============================================================================
B = {'NelSon', 3, true};
res = iscell(B);
assert_istrue(res);
%=============================================================================
B = cell(3, 3, 3);
res = iscell(B);
assert_istrue(res);
