assert_isequal(nargin('ischar'), 1);
assert_isequal(nargout('ischar'), 1);
%=============================================================================
A = 3;
res = ischar(A);
assert_isfalse(res);
%=============================================================================
B = 'NelSon';
res = ischar(B);
assert_istrue(res);
%=============================================================================
C = [1 ; 3];
res = ischar(C);
assert_isfalse(res);
