assert_isequal(nargin('isdouble'), 1);
assert_isequal(nargout('isdouble'), 1);
%=============================================================================
A = 3;
res = isdouble(A);
assert_istrue(res);
%=============================================================================
A = single(3);
res = isdouble(A);
assert_isfalse(res);
%=============================================================================
A = single([3, i]);
res = isdouble(A);
assert_isfalse(res);
%=============================================================================
A = [3, i];
res = isdouble(A);
assert_istrue(res);
