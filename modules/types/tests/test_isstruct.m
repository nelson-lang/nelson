assert_isequal(nargin('isstruct'), 1);
assert_isequal(nargout('isstruct'), 1);
%=============================================================================
A = 1;
res = isstruct(A);
assert_isfalse(res);
%=============================================================================
B = struct();
res = isstruct(B);
assert_istrue(res);
%=============================================================================
C.a = 1;
C.B = 'hello';
res = isstruct(C);
assert_istrue(res);
%=============================================================================
B = struct([]);
res = isstruct(B);
assert_istrue(res);
