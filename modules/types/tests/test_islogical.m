assert_isequal(nargin('islogical'), 1);
assert_isequal(nargout('islogical'), 1);
%=============================================================================
A = 1;
res = islogical(A);
assert_isfalse(res);
%=============================================================================
B = logical(1);
res = islogical(B);
assert_istrue(res);
%=============================================================================
B = sparse(logical(1));
res = islogical(B);
assert_istrue(res);
%=============================================================================
B = true(3, 3, 3);
res = islogical(B);
assert_istrue(res);
