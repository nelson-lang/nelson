assert_isequal(nargin('issparse'), 1);
assert_isequal(nargout('issparse'), 1);
%=============================================================================
A = 1;
res = issparse(A);
assert_isfalse(res);
%=============================================================================
A = sparse(1);
res = issparse(A);
%=============================================================================
A = sparse(true);
res = issparse(A);
assert_istrue(res);
