assert_isequal(second(0), 0);
assert_isequal(second(60000000.12), 48);
assert_isequal(second(60000000.52), 48);
%=============================================================================
M = [datenum('2024-12-11 01:02:01'), datenum('2024-12-11 14:03:02')];
R = second(M);
REF = [1 2];
assert_isequal(R, REF);
%=============================================================================
M = '2024-12-11 22:15:59';
R = second(M);
REF = 59;
assert_isequal(R, REF);
%=============================================================================
M = ["2024-12-11 01:03:33", "2024-12-11 14:02:19"];
R = second(M);
REF = [33 19];
assert_isequal(R, REF);
%=============================================================================
M = datenum({'2024-12-11 03:23:11', '2024-12-11 23:45:13'});
R = second(M);
REF = [11, 13];
assert_isequal(R, REF);
%=============================================================================
M = "2024-12-11 00:00:02";
R = second(M);
REF = 2;
assert_isequal(R, REF);
%=============================================================================
M = [600000.12, 600000.923];
R = second(M);
REF = [48, 7.2];
assert_isapprox(R, REF, 1e-3);
