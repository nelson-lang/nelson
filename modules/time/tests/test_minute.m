assert_isequal(minute(0), 0);
assert_isequal(minute(60000000), 0);
assert_isequal(minute(60000000.52), 28);
%=============================================================================
M = [datenum('2024-12-11 01:02:01'), datenum('2024-12-11 14:03:02')];
R = minute(M);
REF = [2 3];
assert_isequal(R, REF);
%=============================================================================
M = '2024-12-11 22:15:00';
R = minute(M);
REF = 15;
assert_isequal(R, REF);
%=============================================================================
M = ["2024-12-11 01:03:00", "2024-12-11 14:02:00"];
R = minute(M);
REF = [3 2];
assert_isequal(R, REF);
%=============================================================================
M = datenum({'2024-12-11 03:23:00', '2024-12-11 23:45:00'});
R = minute(M);
REF = [23, 45];
assert_isequal(R, REF);
%=============================================================================
M = "2024-12-11 00:00:00";
R = minute(M);
REF = 0;
assert_isequal(R, REF);
%=============================================================================
M = [6000000000.1, 60000000000.9];
R = minute(M);
REF = [24, 36];
assert_isequal(R, REF);
%=============================================================================
