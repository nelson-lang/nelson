assert_isequal(hour(0), 0);
assert_isequal(hour(60000000000000), 0);
assert_isequal(hour(60000000000000.5), 12);
%=============================================================================
M = [datenum('2024-12-11 01:00:00'), datenum('2024-12-11 14:00:00')];
R = hour(M);
REF = [1 14];
assert_isequal(R, REF);
%=============================================================================
M = '2024-12-11 22:15:00';
R = hour(M);
REF = 22;
assert_isequal(R, REF);
%=============================================================================
M = ["2024-12-11 01:00:00", "2024-12-11 14:00:00"];
R = hour(M);
REF = [1 14];
assert_isequal(R, REF);
%=============================================================================
M = datenum({'2024-12-11 03:00:00', '2024-12-11 23:45:00'});
R = hour(M);
REF = [3, 23];
assert_isequal(R, REF);
%=============================================================================
M = "2024-12-11 00:00:00";
R = hour(M);
REF = 0;
assert_isequal(R, REF);
%=============================================================================
M = [6000000000.1, 60000000000.9];
R = hour(M);
REF = [2, 21];
assert_isequal(R, REF);
%=============================================================================
