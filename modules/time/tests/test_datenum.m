assert_isequal(nargin('datenum'), 6);
assert_isequal(nargout('datenum'), 1);
%=============================================================================
d = datenum([1973, 8, 4, 12, 1, 18]);
REF = 720840.500902777770534;
assert_isequal(d, REF);
%=============================================================================
R = datevec(d);
REF = [1973, 8, 4, 12, 1, 18];
assert_isequal(R, REF);
%=============================================================================
assert_isequal(datenum(1973, 8, 4), 720840);
assert_isequal(datenum(1970, 1, 1), 719529);
%=============================================================================
R = datenum([10000, 200000]);
REF = [10000, 200000];
assert_isequal(R, REF);
%=============================================================================
Y = [1973; 1974];
M = [8; 7];
D = [15; 4];
R = datenum(Y, M, D);
REF = [  720851; 721174];
assert_isequal(R, REF);
%=============================================================================
Y = [1973; 1974];
M = [8; 7];
D = [15; 4];
H = [1; 12];
MIN = 30;
S = [0; 1];
R = datenum(Y, M, D, H, MIN, S);
REF = 1.0e+05 * [  7.208510625000000; 7.211745208449074];
assert_isequal(R, REF);
%=============================================================================
Y = [1973; 1974];
M = [8; 7];
D = [15; 4];
R = datenum(Y, M, D);
REF = [720851; 721174];
assert_isequal(R, REF);
