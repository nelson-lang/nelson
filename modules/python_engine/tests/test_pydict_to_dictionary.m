dp = pyrun("dp = {'soup': 3.57, 'bread': 2.29, 'bacon':3.91,'salad':5.00}", "dp");  
dm = dictionary(dp);
REF = dictionary(["soup", "bread", "bacon", "salad"], [3.57, 2.29, 3.91, 5.00]);
assert_isequal(dm, REF);
