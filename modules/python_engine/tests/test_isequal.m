% <--PYTHON ENVIRONMENT REQUIRED-->
%=============================================================================
A = pyrun("", 'A', 'A', [1 2 3]);
B = pyrun("", 'B', 'B', [1 2 3]);
C = pyrun("", 'C', 'C', [3 2 1]);
%=============================================================================
R = isequal(A, A);
assert_istrue(R);
%=============================================================================
R = isequal(A, B);
assert_istrue(R);
%=============================================================================
R = isequal(A, C);
assert_isfalse(R);
%=============================================================================
R = isequal(B, C);
assert_isfalse(R);
%=============================================================================
assert_isequal(A, A);
%=============================================================================
assert_isequal(A, B);
%=============================================================================
A = pyrun("A = ['1', '2', '3']", "A");
B = pyrun("B = ['1', '2', '3']", "B");
C = pyrun("C = 'ff'", "C");
R = isequal(B, C);
assert_isfalse(R);
