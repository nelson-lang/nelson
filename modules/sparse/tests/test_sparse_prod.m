x = sparse([1+i,-i;i,2i]);
r = prod(x);
ref =  sparse([ -1.0 + i, 2]);
assert_isequal(r, ref);
%=============================================================================
x = sparse([true, false; true, false]);
r = prod(x);
assert_isequal(size(r), [1 2]);
assert_isequal(full(r), [1 0]);
