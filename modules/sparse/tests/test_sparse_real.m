A = [1 0 0 0;
0 2 0 0;
0 0 3 0;
0 0 0 4];
CX = sparse (complex(zeros(4, 4), A));
R = real(CX);
REF = sparse(zeros(4,4));
assert_isequal(R, REF);
%=============================================================================
A = [1 0 0 0;
0 2 0 0;
0 0 3 0;
0 0 0 4];
CX = sparse (logical(A));
R = real(CX);
REF = sparse(eye(4, 4));
assert_isequal(R, REF);
