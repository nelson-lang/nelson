R = speye();
REF = sparse(1, 1, 1);
assert_isequal(R, REF);
%=============================================================================
R = speye(4);
assert_isequal(full(R), eye(4));
%=============================================================================
R = speye (2, 4);
assert_isequal(full(R), eye(2, 4));
%=============================================================================
R = speye (4, 2);
assert_isequal(full(R), eye(4, 2));
%=============================================================================
R = speye ([4, 2]);
assert_isequal(full(R), eye(4, 2));
