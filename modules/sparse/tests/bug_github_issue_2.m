% Sparse type: insertion & extraction (minimal implementation).
%=============================================================================
S = sparse([1 2 4], [1 3 5], [10 20 30], 5, 5);
R = S(1:2, :);
REF = sparse([    10     0     0     0     0
     0     0    20     0     0]);
assert_isequal(R, REF);
%=============================================================================
S = sparse([1 2 4], [1 3 5], [10 20 30], 5, 5);
S(1:2, 4:5) = [1 2; 3 4];
REF = sparse([    10     0     0     1     2;
     0     0    20     3     4;
     0     0     0     0     0;
     0     0     0     0    30;
     0     0     0     0     0]);
assert_isequal(S, REF);
%=============================================================================
S = sparse([1 2 4], [1 3 5], [10 20 30], 5, 5);
S(3, :) = [];
REF = sparse([10     0     0     0     0;
     0     0    20     0     0;
     0     0     0     0    30;
     0     0     0     0     0]);
assert_isequal(S, REF);
%=============================================================================
S = sparse([1 2 4], [1 3 5], [10 20 30], 5, 5);
S(:) = [];
REF = sparse(zeros(0,0));
assert_isequal(S, REF);
%=============================================================================
S = sparse([1 2 4], [1 3 5], [10 20 30], 5, 5);
assert_checkerror('S(1, 3) = [];', 'Statement A(...) = [] can only contain one non-colon index.');
