i = [  1,  1,  1,  2,  2,  3,  3,  3,  3 ];
j = [  1,  2,  5,  2,  3,  1,  3,  4,  5 ];
s = [ 11, 12, 15, 22, 23, 31, 33, 34, 35 ];
m = 3;
n = 5;
nz_max = 9;
a = sparse ( i, j, s, m, n, nz_max );
assert_isequal(nzmax(a), nz_max);
%=============================================================================
A = sparse(eye(3, 3));
assert_isequal(nzmax(A), 3);
%=============================================================================
I = [1 2 3];
J = [3 1 2];
V = [32 42 53];
sp = sparse(I, J, V);
assert_isequal(nzmax(sp), 3);
%=============================================================================
A = eye(3, 3);
assert_isequal(nzmax(A), 9);
%=============================================================================
A = [    0     1     2     3;
1     0     0     0;
5     0     1     0];
S = sparse(A);
assert_isequal(nzmax(S), 6);
