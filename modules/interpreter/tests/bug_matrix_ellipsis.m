ans = [];
A = 1
[ ...
 A, ...
 A ...
]
assert_isequal(ans, [1 1]);
%=============================================================================
A = 2
[ ...
 A; ...
 A ...
]
assert_isequal(ans, [2; 2]);
%=============================================================================
A = [1 2; 3 4]
[ ...
 A, ...
 A ...
]
assert_isequal(ans, [1 2 1 2; 3 4 3 4]);
%=============================================================================
A = [1 2; 3 4]
[ ...
 A; ...
 A ...
]   
assert_isequal(ans, [1 2; 3 4; 1 2; 3 4]);
%=============================================================================
A = [1 2; 3 4]
[ ...
 A(1, :), ...
 A(2, :) ...
]
assert_isequal(ans, [1 2 3 4]);
%=============================================================================
A = [1 2; 3 4]
[ ...
 A(:, 1); ...
 A(:, 2) ...
]
assert_isequal(ans, [1; 3; 2; 4]);
