nested_function = sprintf(['function y = nested_parser_case(x)\n', ...
  '  y = nested_helper(x);\n', ...
  '  function z = nested_helper(t)\n', ...
  '    z = t + 1;\n', ...
  '  end\n', ...
  'end']);
assert_isequal(parsestring(nested_function), 'function');
%=============================================================================
nested_comment_function = sprintf(['function y = nested_comment_case(x)\n', ...
  '  %%{\n', ...
  '  function z = not_a_nested_function(t)\n', ...
  '    z = t + 1;\n', ...
  '  end\n', ...
  '  %%}\n', ...
  '  y = x + 1;\n', ...
  'end']);
assert_isequal(parsestring(nested_comment_function), 'function');
