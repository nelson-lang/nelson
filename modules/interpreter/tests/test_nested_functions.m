path_test = [tempdir(), createGUID()];
mkdir(path_test);
filename = [path_test, '/nested_closure_factory.m'];
filewrite(filename, ["function h = nested_closure_factory(seed)"; ...
  "  base = seed;"; ...
  "  h = @nested_closure_step;"; ...
  "  function y = nested_closure_step(delta)"; ...
  "    base = base + delta;"; ...
  "    y = base;"; ...
  "  end"; ...
  "end"]);
addpath(path_test);
h1 = nested_closure_factory(10);
h2 = nested_closure_factory(100);
assert_isequal(h1(2), 12);
assert_isequal(h1(3), 15);
assert_isequal(h2(5), 105);
assert_isequal(h1(1), 16);
assert_isequal(func2str(h1), 'nested_closure_step');
rmpath(path_test);
rmdir(path_test, 's');
