path_test = [tempdir(), createGUID()];
mkdir(path_test);
filename = [path_test, '/nested_runtime_case.m'];
filewrite(filename, ["function y = nested_runtime_case(x)"; ...
  "  base = 10;"; ...
  "  y = nested_helper(x);"; ...
  "  function z = nested_helper(t)"; ...
  "    z = t + base;"; ...
  "    base = base + 5;"; ...
  "  end"; ...
  "  y = y + base;"; ...
  "end"]);
addpath(path_test);
assert_isequal(nested_runtime_case(2), 27);
rmpath(path_test);
rmdir(path_test, 's');
