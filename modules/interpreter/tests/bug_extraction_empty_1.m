A = rand(2, 4);
R = A(A > 1);
assert_istrue(isempty(R));
assert_isequal(size(R), [0 1]);
%=============================================================================
A = rand(2, 4);
A(A > 1, 2)
assert_istrue(isempty(R));
assert_isequal(size(R), [0 1]);
%=============================================================================
A(A(:,1)>1, :)
assert_istrue(isempty(R));
assert_isequal(size(R), [0 1]);
