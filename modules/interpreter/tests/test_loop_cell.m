R = cell(2, 3);
for k = 1:6
  R{k} = k;
end
REF = {1, 3, 5; 2, 4, 6};
assert_isequal(R, REF)
%=============================================================================
R = zeros(2, 3);
for k = 1:6
  R(k) = k;
end
REF = [1, 3, 5; 2, 4, 6];
assert_isequal(R, REF)
