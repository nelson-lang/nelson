local_after_function_then_script = sprintf(['x = localBeforeRest(1);\n', ...
  'function y = localBeforeRest(x)\n', ...
  '  y = x + 1;\n', ...
  'end\n', ...
  'function y = secondLocalBeforeRest(x)\n', ...
  '  y = x + 2;\n', ...
  'end\n', ...
  'z = x + 1;']);
