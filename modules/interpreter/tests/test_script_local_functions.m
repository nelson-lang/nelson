mixed_script = sprintf(['mixedValue = mixedLocalHelper(41);\n', ...
  'function y = mixedLocalHelper(x)\n', ...
  '  y = x + 1;\n', ...
  'end']);
assert_isequal(parsestring(mixed_script), 'script');
%=============================================================================
non_trailing_local = sprintf(['x = localBeforeRest(1);\n', ...
  'function y = localBeforeRest(x)\n', ...
  '  y = x + 1;\n', ...
  'end\n', ...
  'z = x + 1;']);
assert_isequal(parsestring(non_trailing_local), 'error');
%=============================================================================
local_in_if = sprintf(['if true\n', ...
  '  function y = localInsideIf(x)\n', ...
  '    y = x;\n', ...
  '  end\n', ...
  'end']);
assert_isequal(parsestring(local_in_if), 'error');
%=============================================================================
local_in_for = sprintf(['for k = 1:2\n', ...
