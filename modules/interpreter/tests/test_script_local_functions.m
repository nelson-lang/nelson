path_test = [tempdir(), createGUID()];
mkdir(path_test);
filename = [path_test, '/mixed_script_local_test.m'];
filewrite(filename, ["mixedValue = mixedLocalHelper(41);"; ...
  "mixedAgain = mixedSecondLocal(mixedValue);"; ...
  "function y = mixedLocalHelper(x)"; ...
  "  y = x + 1;"; ...
  "end"; ...
  "function y = mixedSecondLocal(x)"; ...
  "  y = x * 2;"; ...
  "end"]);
run(filename);
assert_isequal(mixedValue, 42);
assert_isequal(mixedAgain, 84);
rmdir(path_test, 's');
