R = eval('3.''');
assert_isequal(R, 3);
%=============================================================================
cmd = '3. ''';
msg = _('Lexical error ''unterminated character array''');
assert_checkerror(cmd, msg);
%=============================================================================
R = eval('3 .''');
assert_isequal(R, 3);
%=============================================================================
R = eval('3.12 .''');
assert_isequal(R, 3.12);
