addpath([nelsonroot(), '/modules/interpreter/tests/']);
%=============================================================================
A = [1 4 7 10; 2 5 8 11; 3 6 9 12];
%=============================================================================
R = A(2, [1 3 4]);
assert_isequal(R, [2 8 11]);
%=============================================================================
R = A(1:2, 2:3);
assert_isequal(R, [4 7; 5 8]);
%=============================================================================
B = A;
B(2, [1 3]) = [20 80];
REF = [1 4 7 10; 20 5 80 11; 3 6 9 12];
assert_isequal(B, REF);
%=============================================================================
B = A;
B(:, 2) = [40; 50; 60];
REF = [1 40 7 10; 2 50 8 11; 3 60 9 12];
assert_isequal(B, REF);
%=============================================================================
C = {1, 2; 3, 4};
R = C{2, 1};
assert_isequal(R, 3);
%=============================================================================
R = C(1, :);
assert_isequal(R, {1, 2});
%=============================================================================
C{1, 2} = 22;
assert_isequal(C{1, 2}, 22);
%=============================================================================
S.a = [10 20 30];
R = S.a(2);
assert_isequal(R, 20);
%=============================================================================
S.a(3) = 99;
assert_isequal(S.a, [10 20 99]);
%=============================================================================
missingFile = [tempdir(), 'nelson_missing_file_for_empty_struct_field_check.html'];
fileInfo = dir(missingFile);
assert_istrue(isempty(fileInfo.datenum));
%=============================================================================
S(1).name = 'alpha';
S(2).name = 'beta';
S(3).name = 'gamma';
fieldname = 'name';
R = {S.(fieldname)};
assert_isequal(R, {'alpha', 'beta', 'gamma'});
%=============================================================================
folders = unique_if_exists_for_bytecode_test(S, fieldname);
assert_isequal(folders, {'alpha', 'beta', 'gamma'});
