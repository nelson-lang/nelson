assert_isequal(nargin('lastwarn'), 2);
assert_isequal(nargout('lastwarn'), 2);
%=============================================================================
lastwarn();
%=============================================================================
msg = lastwarn();
assert_isequal(msg, '');
%=============================================================================
[msg, id] = lastwarn();
assert_isequal(msg, '');
assert_isequal(id, '');
%=============================================================================
lastwarn('toto');
[msg, id] = lastwarn();
assert_isequal(msg, 'toto');
assert_isequal(id, '');
%=============================================================================
lastwarn('');
[msg, id] = lastwarn();
assert_isequal(msg, '');
assert_isequal(id, '');
%=============================================================================
lastwarn('toto','titi');
[msg, id] = lastwarn();
assert_isequal(msg, 'toto');
assert_isequal(id, 'titi');
%=============================================================================
lastwarn('');
[msg, id] = lastwarn();
assert_isequal(msg, '');
assert_isequal(id, '');
