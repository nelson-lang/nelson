assert_isequal(nargin('std'), -1);
assert_isequal(nargout('std'), -1);
%=============================================================================
R = std([1,2,3], 0, 1);
REF = [NaN NaN NaN];
assert_isequal(R, REF);
%=============================================================================
A = [4 -7 3; 1 4 -2; 10 7 9];
R = std(A);
REF = [4.5826    7.3711    5.5076];
assert_isapprox(R, REF, 1e-4);
%=============================================================================
A = [4 -2 1; 9 5 7];
R = std (A, 0, 1);
REF = [3.5355    4.9497    4.2426];
assert_isapprox(R, REF, 1e-4);
%=============================================================================
A = [4 -2 1; 9 5 7];
R = std(A, 0, 2);
REF = [3; 2];
assert_isapprox(R, REF, 1e-4);
%=============================================================================
assert_checkerror('std()', _('Wrong number of input arguments.'))
%=============================================================================
assert_isequal(std(36), 0);
