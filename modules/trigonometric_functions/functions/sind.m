function r = sind(x)
  if isreal(x)
    r = sindReal(x);
  else
    r = sindComplex(x);
  end
end
%=============================================================================
function r = sindReal(x)
  x = mod (real(x) - 180, 360) - 180;
  r = sin (x / 180 * pi);
  r(x == -180) = 0;
end
%=============================================================================
function r = sindComplex(x)
  ix = imag(x);
  x = mod(real(x) - 180, 360) - 180;
  r = sin(complex (x, ix) / 180 * pi);
  r(x == -180) = complex (0, imag (r(x == -180)));
end
