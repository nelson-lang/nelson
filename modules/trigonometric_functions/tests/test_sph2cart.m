assert_isequal(nargin('sph2cart'), -1);
assert_isequal(nargout('sph2cart'), -1);
%=============================================================================
azimut = [0.7854, 0.7854, -0.7854, -0.7854; 2.3562, 2.3562, -2.3562, -2.3562];
elevation = [0.6155, -0.6155, 0.6155, -0.6155; 0.6155, -0.6155, 0.6155, -0.6155];
radius = 1.7321 * ones(2, 4);
[x, y, z] = sph2cart(azimut, elevation, radius);
x_REF = [1.0000    1.0000    1.0000    1.0000;
   -1.0000   -1.0000   -1.0000   -1.0000];
y_REF = [1.0000    1.0000   -1.0000   -1.0000;
    1.0000    1.0000   -1.0000   -1.0000];
z_REF = [1.0001   -1.0001    1.0001   -1.0001;
    1.0001   -1.0001    1.0001   -1.0001];
assert_isapprox(x, x_REF, 1e-4);
assert_isapprox(y, y_REF, 1e-4);    
assert_isapprox(z, z_REF, 1e-4);   
