assert_isequal(nargout('sec'), 1);
%=============================================================================
x = -pi:0.75:pi;
R = sec(x);
REF = [ -1.0000,   -1.3667,  -14.1368,    1.5919,    1.0101,    1.2187,    4.7439,   -1.9528,   -1.0415];
assert_isapprox(R, REF, 1e-4);
