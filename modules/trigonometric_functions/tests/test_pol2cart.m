assert_isequal(nargin('pol2cart'), -1);
assert_isequal(nargout('pol2cart'), -1);
%=============================================================================
theta = [0 pi/4 pi/2 pi];
rho = [5 5 10 10];
[x, y] = pol2cart(theta, rho);
x_REF = [5.0000    3.5355    0.0000  -10.0000];
y_REF = [0    3.5355   10.0000    0.0000];
assert_isapprox(x, x_REF, 1e-4);
assert_isapprox(y, y_REF, 1e-4);
%=============================================================================
theta = [0    0.7854    1.5708    3.1416];
rho = [1.0000    3.0000    4.0000    5.0000];
el = [7     8     9    10];
[x, y, z] = pol2cart(theta, rho, el);
x_REF = [1 2.1213 0 -5];
y_REF = [0 2.1213 4 0];
z_REF = [7 8 9 10];
assert_isapprox(x, x_REF, 1e-4);
assert_isapprox(y, y_REF, 1e-4);
assert_isapprox(z, z_REF, 1e-4);
