assert_isequal(nargin('asinh'), -1);
assert_isequal(nargout('asinh'), 1);
%=============================================================================
R = asinh([-i, 0, 1/6, i/2, i, 2]);
REF = [0.0000 - 1.5708i, 0.0000 + 0.0000i, 0.1659 + 0.0000i, 0.0000 + 0.5236i, 0.0000 + 1.5708i, 1.4436 + 0.0000i];
assert_isapprox(R, REF, 1e-4);
%=============================================================================
X = [2 -3 1+2i];
R = asinh(X);
REF = [1.4436 + 0.0000i, -1.8184 + 0.0000i, 1.4694 + 1.0634i];
assert_isapprox(R, REF, 1e-4);
