A = [1; NaN; 3];
R = fillmissing(A, 'constant', 0);
assert_isequal(R, [1; 0; 3]);
%=============================================================================
A = [1; NaN; NaN; 4];
R = fillmissing(A, 'previous');
assert_isequal(R, [1; 1; 1; 4]);
%=============================================================================
[R, TF] = fillmissing([1; NaN; 3], 'linear');
assert_isequal(R, [1; 2; 3]);
assert_isequal(TF, [false; true; false]);
