T = table({'a'; 'a'; 'b'}, [1; 2; 4], [10; 20; 30], 'VariableNames', {'G', 'X', 'Y'});
R = groupsummary(T, 'G', 'sum', {'X', 'Y'});
assert_isequal(R.sum_X, [3; 4]);
assert_isequal(R.sum_Y, [30; 30]);
