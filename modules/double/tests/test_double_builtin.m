assert_isequal(nargin('double'), 1);
assert_isequal(nargout('double'), 1);
%=============================================================================
A = ["1", "2+i", "+nan"];
R = double(A);
REF = [1 2+i NaN];
assert_isequal(R, REF);
%=============================================================================
A = "Inf+Infi";
R = double(A);
REF = complex(Inf, Inf);
assert_isequal(R, REF);
%=============================================================================
A = logical(1);
R = double(A);
REF = 1;
assert_isequal(R, REF);
