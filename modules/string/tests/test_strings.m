assert_isequal(nargin('strings'), -1);
assert_isequal(nargout('string'), 1);
%=============================================================================
R = strings();
REF = "";
assert_isequal(R, REF);
%=============================================================================
R = strings(3, 2);
REF = ["", ""; "", ""; "", ""];
assert_isequal(R, REF);
%=============================================================================
R = strings([3, 2]);
REF = ["", ""; "", ""; "", ""];
assert_isequal(R, REF);
