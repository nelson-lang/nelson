assert_isequal(nargin('sprintf'), -1);
assert_isequal(nargout('sprintf'), 2);
%=============================================================================
R = sprintf('%s','hello');
REF = 'hello';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%s %s', 'hello', 'Nelson');
REF = 'hello Nelson';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%f', -53);
REF = '-53.000000';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%f', 55.55);
REF = '55.550000';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%f',0.00563);
REF = '0.005630';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%f',0.0000000123456);
REF = '0.000000';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%f',22244234534545);
REF = '22244234534545.000000';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%g', -53);
REF = '-53';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%g', 55.55);
REF = '55.55';
