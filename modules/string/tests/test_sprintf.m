%=============================================================================
R = sprintf('%s', ['a','b','c';'d','e','f']);
REF = 'adbecf';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%s', []);
REF = char(zeros(1, 0));
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%d', []);
REF = char(zeros(1, 0));
assert_isequal(R, REF);
%=============================================================================
R = sprintf("%f",0.00563);
REF = "0.005630";
assert_isequal(R, REF);
%=============================================================================
R = sprintf("%s", ["a","b","c";"d","e","f"]);
REF = "adbecf";
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%s','20 %.');
REF = '20 %.';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%d%%.',50);
REF = '50%.';
assert_isequal(R, REF);
