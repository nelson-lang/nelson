assert_isequal(R, REF);
%=============================================================================
R = sprintf('%g',22244234534545);
REF = '2.22442e+13';
assert_isequal(R, REF);
%=============================================================================
R = sprintf('%G', -53);
REF = '-53';
assert_isequal(R, REF);
%=============================================================================
