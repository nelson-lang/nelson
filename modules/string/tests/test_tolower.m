assert_isequal(nargin('tolower'), 1);
assert_isequal(nargout('tolower'), 1);
%=============================================================================
A = 'Nelson';
R = tolower(A);
REF = 'nelson';
assert_isequal(R, REF);
%=============================================================================
A = {'r', 'B', 'C'};
R = tolower(A);
REF = {'r', 'b', 'c'};
assert_isequal(R, REF);
%=============================================================================
A = ["r", "b", string(NaN) , "C"];
R = tolower(A);
REF = ["r", "b", string(NaN), "c"];
assert_isequal(R, REF);
