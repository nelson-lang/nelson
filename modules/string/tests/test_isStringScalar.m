assert_isequal(nargin('isStringScalar'), 1);
assert_isequal(nargout('isStringScalar'), 1);
%=============================================================================
R = isStringScalar('hello');
assert_isfalse(R);
%=============================================================================
R = isStringScalar("hello");
assert_istrue(R);
%=============================================================================
R = isStringScalar(string(NaN));
assert_istrue(R);
%=============================================================================
R = isStringScalar(string([]));
assert_isfalse(R);
%=============================================================================
R = isStringScalar(["hello", "world"]);
assert_isfalse(R);
