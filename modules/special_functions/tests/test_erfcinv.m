assert_isequal(nargin('erfcinv'), 1)
assert_isequal(nargout('erfcinv'), 1)
%=============================================================================
R = erfcinv(1);
assert_isequal(R, 0);
%=============================================================================
R = erfcinv([-10 0 0.5 1.3 2 Inf]);
REF = [NaN Inf 0.47693627620447 -0.272462714726754 -Inf NaN];
assert_isapprox(R, REF, 1e-12);
%=============================================================================
X = [0.25 0.75; 1.25 1.75];
R = erfcinv(X);
assert_isequal(size(R), [2 2]);
assert_isapprox(erfc(R), X, 1e-12);
%=============================================================================
R = erfcinv(single([1 0.5]));
REF = single([0 0.47693627620447]);
assert_isequal(class(R), 'single');
assert_isapprox(R, REF, 1e-6);
%=============================================================================
assert_checkerror('R = erfcinv(1 + i);', _('Input argument must be dense and real.'));
