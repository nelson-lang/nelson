assert_isequal(nargin('interp1'), -1);
assert_isequal(nargout('interp1'), -1);
%=============================================================================
REF = [0    0.1768    0.3536    0.5303    0.7071    0.7803    0.8536    0.9268    1.0000  ...
0.9268    0.8536    0.7803    0.7071    0.5303    0.3536    0.1768    0.0000   -0.1768 ...
-0.3536   -0.5303   -0.7071   -0.7803   -0.8536   -0.9268   -1.0000   -0.9268   -0.8536 ...
-0.7803   -0.7071   -0.5303   -0.3536   -0.1768   -0.0000];
x = 0:pi/4:2*pi; 
v = sin(x);
xq = 0:pi/16:2*pi;
R = interp1(x, v, xq);
assert_isapprox(R, REF, 1e-3);
%=============================================================================
R = interp1(x, v, xq, 'linear');
assert_isapprox(R, REF, 1e-3);
%=============================================================================
REF = [0.7050    1.7050    1.7050    0.7050   -0.7050   -1.7050   -1.7050   -0.7050];
v = [0  1.41  2  1.41  0  -1.41  -2  -1.41 0];
xq = 1.5:8.5;
R = interp1(v, xq);
assert_isapprox(R, REF, 1e-3);
%=============================================================================
R = interp1(v, xq, 'linear');
assert_isapprox(R, REF, 1e-3);
%=============================================================================
v = [0  1.41  2  1.41  0  -1.41  -2  -1.41 0] + 2i;
xq = 1.5:8.5;
R = interp1(v, xq);
REF = [ 0.7050 + 2.0000i   1.7050 + 2.0000i   1.7050 + 2.0000i   0.7050 + 2.0000i ...
-0.7050 + 2.0000i -1.7050 + 2.0000i  -1.7050 + 2.0000i  -0.7050 + 2.0000i];
assert_isapprox(R, REF);
%=============================================================================
x = 0:pi/4:2*pi; 
v = single(sin(x));
xq = 0:pi/16:2*pi;
R = interp1(x, v, xq);
assert_istrue(issingle(R));
%=============================================================================
values = [0.18995 0.07176 0.23217;0.19483 0.08339 0.26149];
n = 5;
P = size(values, 1);
vq = interp1(1:P, values, linspace(1, P, n), 'linear');
REF = [0.1900    0.0718    0.2322;
0.1912    0.0747    0.2395;
0.1924    0.0776    0.2468;
0.1936    0.0805    0.2542;
0.1948    0.0834    0.2615];
assert_isapprox(vq, REF, 1e-3);
%=============================================================================
x = 0:4;
v = x .^ 2;
R = interp1(x, v, [-1, 0, 1], 'linear', 9);
assert_isapprox(R, [9, 0, 1], 1e-12);
%=============================================================================
x = 1:4;
v = [10 20 40 80];
R = interp1(x, v, [1.2 1.8 2.5 3.7], 'nearest');
assert_isequal(R, [10 20 20 80]);
%=============================================================================
R = interp1(x, v, [1.2 1.8 2.5 3.7], 'previous');
assert_isequal(R, [10 10 20 40]);
%=============================================================================
R = interp1(x, v, [1.2 1.8 2.5 3.7], 'next');
assert_isequal(R, [20 20 40 80]);
%=============================================================================
R = interp1(x, v, [0 1.5 5], 'pchip');
assert_istrue(all(isfinite(R)));
%=============================================================================
pp = interp1(x, v, 'linear', 'pp');
R = ppval(pp, [1.5 2.5]);
assert_isapprox(R, [15 30], 1e-12);
%=============================================================================
x = 1:10;
v = (5 * x) + (x .^ 2) * i;
xq = 1:0.25:10;
R = interp1(x, v, xq);
assert_isapprox(R(1), 5 + 1i, 1e-12);
assert_isapprox(R(end), 50 + 100i, 1e-12);
%=============================================================================
x = 4:-1:1;
v = [80 40 20 10];
R = interp1(x, v, 2.5);
assert_isapprox(R, 30, 1e-12);
%=============================================================================
x = 1:4;
v = [1 2; 3 4; 5 6; 7 8];
xq = [1 2; 3 4];
R = interp1(x, v, xq);
assert_isequal(size(R), [2 2 2]);
assert_isapprox(R(1, 1, 1), 1, 1e-12);
assert_isapprox(R(2, 2, 2), 8, 1e-12);
%=============================================================================
R = interp1(1:4, [10 20 40 80], [0 5]);
assert_istrue(all(isnan(R)));
