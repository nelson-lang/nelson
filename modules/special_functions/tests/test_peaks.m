assert_isequal(nargin('peaks'), -1);
assert_isequal(nargout('peaks'), -1);
%=============================================================================
R = peaks([]);
assert_isequal(R, []);
%=============================================================================
R = peaks(2);
REF = [6.671280296717442e-05 ,   -5.864187872589529e-06;
3.223535961269272e-05     4.102972745826758e-05];
assert_isapprox(R, REF, 1e-8);
%=============================================================================
[X, Y, Z] = peaks(2);
X_REF = [-3     3; -3     3];
Y_REF = [-3    -3; 3     3];
Z_REF = [6.671280296717442e-05 ,   -5.864187872589529e-06;
3.223535961269272e-05     4.102972745826758e-05];
assert_isequal(X, X_REF);
assert_isequal(Y, Y_REF);
assert_isapprox(Z, Z_REF, 1e-8);
