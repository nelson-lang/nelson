V = reshape(1:8, [2 2 2]);
R = interp3(V, 1.5, 1.5, 1.5);
assert_isapprox(R, 4.5, 1e-12);
%=============================================================================
R = interp3(V, 1.2, 1.8, 1.1, 'nearest');
assert_isequal(R, V(2, 1, 1));
%=============================================================================
R = interp3(V, 0, 1.5, 1.5, 'linear', -1);
assert_isequal(R, -1);
%=============================================================================
Rq = interp3(V);
assert_isequal(size(Rq), [3 3 3]);
assert_isapprox(Rq(2, 2, 2), 4.5, 1e-12);
%=============================================================================
R = interp3(V, 0);
assert_isequal(R, V);
%=============================================================================
R = interp3(V + 3i, 1.5, 1.5, 1.5);
assert_isapprox(R, 4.5 + 3i, 1e-12);
%=============================================================================
x = [2 1];
y = [2; 1];
z = [2 1];
R = interp3(x, y, z, V, 1.5, 1.5, 1.5);
assert_isapprox(R, 4.5, 1e-12);
%=============================================================================
[X, Y, Z] = meshgrid(1:2, 1:2, 1:2);
R = interp3(X, Y, Z, V, 1.5, 1.5, 1.5, 'linear');
assert_isapprox(R, 4.5, 1e-12);
