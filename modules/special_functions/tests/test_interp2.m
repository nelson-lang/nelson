V = [1 2; 3 4];
R = interp2(V, 1.5, 1.5);
assert_isapprox(R, 2.5, 1e-12);
%=============================================================================
R = interp2(V, 1.2, 1.8, 'nearest');
assert_isequal(R, 3);
%=============================================================================
R = interp2(V, 0, 1.5, 'linear', -1);
assert_isequal(R, -1);
%=============================================================================
Rq = interp2(V);
assert_isequal(size(Rq), [3 3]);
assert_isapprox(Rq(2, 2), 2.5, 1e-12);
%=============================================================================
x = [10 20];
y = [1; 3];
R = interp2(x, y, V, 15, 2);
assert_isapprox(R, 2.5, 1e-12);
%=============================================================================
R = interp2(V, 0);
assert_isequal(R, V);
%=============================================================================
R = interp2(V + 2i, 1.5, 1.5);
assert_isapprox(R, 2.5 + 2i, 1e-12);
%=============================================================================
x = [2 1];
y = [2; 1];
R = interp2(x, y, V, 1.5, 1.5);
assert_isapprox(R, 2.5, 1e-12);
%=============================================================================
[X, Y] = meshgrid(-3:3);
P = peaks(X, Y);
[Xq, Yq] = meshgrid(-3:0.5:3);
R = interp2(X, Y, P, Xq, Yq, 'linear');
assert_isequal(size(R), size(Xq));
assert_istrue(all(isfinite(R(:))));
%=============================================================================
Xq = [1.25 1.75; 1.25 1.75];
Yq = [1.25 1.25; 1.75 1.75];
R = interp2(V, Xq, Yq);
assert_isequal(size(R), [2 2]);
