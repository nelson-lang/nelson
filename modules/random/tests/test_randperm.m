R = randperm(6);
REF = [6     3     5     1     2     4];
assert_isequal(R, REF);
%=============================================================================
R = randperm(-6);
REF = ones(1, 0);
assert_isequal(R, REF);
%=============================================================================
R = randperm(0);
REF = ones(1, 0);
assert_isequal(R, REF);
