wstringVector
QtConstants()
{
    if (mapConstants.empty()) {
        initializeQtConstantsMap();
    }
    wstringVector names;
    for (auto& mapConstant : mapConstants) {
        names.push_back(mapConstant.first);
    }
    return names;
}
