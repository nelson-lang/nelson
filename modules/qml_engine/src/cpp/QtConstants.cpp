ArrayOf
QtConstant(std::wstring name, bool& found)
{
    ArrayOf res;
    found = false;
    if (mapConstants.empty()) {
        initializeQtConstantsMap();
    }
    std::map<std::wstring, int>::iterator it = mapConstants.find(name);
    if (it != mapConstants.end()) {
        res = ArrayOf::doubleConstructor(mapConstants[name]);
        found = true;
    }
    return res;
}
