T = table([1;2;3],[2 8; 4 10; 6 12],[3 12 21; 6 15 24; 9 18 27], 'VariableNames',["One" "Two" "Three"]);
R = table2array(T);
REF = [1     2     8     3    12    21;
    2     4    10     6    15    24;
    3     6    12     9    18    27];
assert_isequal(R, REF);
%=============================================================================
T = table(ones(2,1,3),2*ones(2,2,3),3*ones(2,3,3),'VariableNames',["One" "Two" "Three"]);
A = table2array(T);
assert_isequal(size(A),[  2     6     3]);
