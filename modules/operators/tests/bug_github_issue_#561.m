R = 0^0;
REF = 1;
assert_isequal(R, REF);
%=============================================================================
R = complex(0, 0) ^ 0;
REF = 1;
assert_isequal(R, REF)
%=============================================================================
R = 0 ^ complex(0, 0);
REF = 1;
assert_isequal(R, REF)
%=============================================================================
R = complex(0, 0) ^ complex(0, 0);
REF = 1;
assert_isequal(R, REF)
%=============================================================================
A = [1 2; 3 4];
R = A^0;
REF = [  1     0; 0     1];
assert_isequal(R, REF)
