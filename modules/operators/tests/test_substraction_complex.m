A = 1:2;
B = (1:3)';
R = B*i - A;
REF = [-1.0000+1.0000i,  -2.0000+1.0000i;
-1.0000+2.0000i,  -2.0000+2.0000i;
-1.0000+3.0000i,  -2.0000+3.0000i];
assert_isequal(R, REF);
