A = magic(5);
S.type='()';
S.subs={1:2,':'};
R = subsref(A, S);
REF = [    17    24     1     8    15;
23     5     7    14    16];
assert_isequal(R, REF);
%=============================================================================
C = {"one", 2, 'three'};
S = [];
S.type = '{}';
S.subs = {[1 2]};
[R1, R2] = subsref(C, S);
assert_isequal(R1, "one");
assert_isequal(R2, 2);
%=============================================================================
A = struct('number', 10);
S = [];
S.type = '.';
S.subs = 'number';
R = subsref(A, S)
assert_isequal(R, 10);
