R = mag2db(0.1);
REF = -20;
assert_isequal(R, REF);
%=============================================================================
R = mag2db(magic(3))
REF = [ 18.0618         0   15.5630;
9.5424   13.9794   16.9020;
12.0412   19.0849    6.0206];
assert_isapprox(R, REF, 1e-3);
%=============================================================================
R = mag2db([])
REF = [];
assert_isequal(R, REF);
%=============================================================================
R = mag2db(-64);
REF = NaN;
assert_isequal(R, REF);
