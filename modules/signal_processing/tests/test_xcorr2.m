A = [1 2 1; 0 1 0; -1 -2 -1];
B = [1 1 1; 1 0 1; 1 -1 -1];
R = xcorr2(A, B);
REF = [    -1    -3    -2     1     1;
     1     1     1     3     1;
     2     7     6     3     0;
    -1    -1    -1    -1    -1;
    -1    -3    -4    -3    -1];
assert_isequal(R, REF);
